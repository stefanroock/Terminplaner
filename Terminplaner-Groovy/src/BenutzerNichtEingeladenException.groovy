
public class BenutzerNichtEingeladenException extends Exception {

	BenutzerNichtEingeladenException() {
		super();
	}

	BenutzerNichtEingeladenException(String message, Throwable cause) {
		super(message, cause);
	}

	BenutzerNichtEingeladenException(String message) {
		super(message);
	}

	BenutzerNichtEingeladenException(Throwable cause) {
		super(cause);
	}
	
}
