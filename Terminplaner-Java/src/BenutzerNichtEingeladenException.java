public class BenutzerNichtEingeladenException extends Exception {

	public BenutzerNichtEingeladenException() {
		super();
	}

	public BenutzerNichtEingeladenException(String message, Throwable cause) {
		super(message, cause);
	}

	public BenutzerNichtEingeladenException(String message) {
		super(message);
	}

	public BenutzerNichtEingeladenException(Throwable cause) {
		super(cause);
	}
	
}
